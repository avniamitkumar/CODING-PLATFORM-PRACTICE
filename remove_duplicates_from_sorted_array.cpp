class Solution{
public:
    int remove_duplicate(int a[],int n){
        // code here
        int prev=-1;
        int cnt=0;
        for(int i=0;i<n;i++)
        {
            if(a[i]==prev)
            {
                prev=a[i];
            }
            else
            {
                a[cnt]=a[i];
                cnt++;
                prev=a[i];
            }
            
        }
        return cnt;
        
    }
};

